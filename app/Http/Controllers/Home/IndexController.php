     */
    public function getcates($pid){
        $cate = [];
        $s = DB::table('type')->where('pid','=',$pid)->get();
        foreach($s as $key=>$value){
            $value->dev = $this->getcates($value->id);
            $cate[]=$value;
        }
        return $cate;
    }
